import { execSync } from "child_process";
import { mkdirSync, writeFileSync } from "fs";
import { dirname } from "path";

const OUTPUT_FILE = "src/generated/build-info.ts";
const ISO_DATE_REGEX = /^\d{4}-\d{2}-\d{2}$/;

const getTodayInUtc = () => new Date().toISOString().slice(0, 10);

const getLastCommitDate = () => {
  const date = execSync("git log -1 --format=%cd --date=format:%Y-%m-%d", {
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"],
  }).trim();

  if (!ISO_DATE_REGEX.test(date)) {
    throw new Error(`Unexpected git date format: "${date}"`);
  }

  return date;
};

try {
  const date = getLastCommitDate();
  mkdirSync(dirname(OUTPUT_FILE), { recursive: true });
  writeFileSync(
    OUTPUT_FILE,
    `// Auto-generated by generate-build-info.mjs\nexport const LAST_UPDATE = "${date}" as const;\n`,
    "utf8",
  );
  console.log(`Build date injected (${OUTPUT_FILE}):`, date);
} catch (error) {
  const fallbackDate = getTodayInUtc();
  mkdirSync(dirname(OUTPUT_FILE), { recursive: true });
  writeFileSync(
    OUTPUT_FILE,
    `// Auto-generated by generate-build-info.mjs\nexport const LAST_UPDATE = "${fallbackDate}" as const;\n`,
    "utf8",
  );
  console.warn(
    `Could not read git history, falling back to ${fallbackDate}.`,
    error instanceof Error ? error.message : error,
  );
}
